     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => ['required', 'min:3', 'string'],
            'mail' => ['required','email','unique'],
            'tel' => ['required'],
            'message' => 'nullable'
        ]);
    
        $contact = Contact::create([
            'name'=>$request->name,
            'email'=>$request->mail,
            'phone'=>$request->tel,
            'message'=>$request->message
        ]);
    
        return redirect()->route('contact.index');
